package org.apache.karaf.cave.deployer.api;

/**
 * Simple wrapper describing a connection to a Karaf instance.
 */
public class Connection {

    private String name;
    private String jmxUrl;
    private String karafName;
    private String user;
    private String password;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getJmxUrl() {
        return jmxUrl;
    }

    public void setJmxUrl(String jmxUrl) {
        this.jmxUrl = jmxUrl;
    }

    public String getKarafName() {
        return karafName;
    }

    public void setKarafName(String karafName) {
        this.karafName = karafName;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

}
